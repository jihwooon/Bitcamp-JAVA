class WhileTest1 
{
	public static void main(String[] args) 
	{
		int i=0;
			
		while(i<5){//조건식	
						
			++i;//++i, i=i=1; i+=1;
			System.out.println(i);//순서가 바뀌면 결과값도 바뀐다.
	
		}

		System.out.println("The End...");
	}
}
